x = 0
print("Primer While")
while x <= 15:
	print(x)
	x += 5

x = 0
print("\nSegundo While")
while x >= -100:
	print(x)
	x -= 20

x = 10
print("\nTercer While")
while x >= 0:
	print('El valor de x es: ', x)
	x -= 1

print("\n\n")